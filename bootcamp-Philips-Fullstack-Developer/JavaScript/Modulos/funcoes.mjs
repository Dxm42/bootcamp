function monstraIdade(nome, idade){
    return `A idade de ${nome} é ${idade}`;
}
function monstraCidade(nome, cidade){
    return `A cidade de ${nome} é ${cidade}`;
}
function monstraHobby(nome, hobby){
    return `O hobby de ${nome} é ${hobby}`;
}

export {monstraIdade, monstraCidade, monstraHobby};